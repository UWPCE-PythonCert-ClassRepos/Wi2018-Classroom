"""
This class will add two values together
"""


class Subtracter(object):
    
    @staticmethod
    def calc(operand_1, operand_2):
    	"""
    	This method will subtract two values
    	operand_1: The first value in the 
    	subtraction equation
    	operand_2: The second value in the 
    	subtraction equation
    	return: Returns the difference between 
    	operand_1 and operand_2
    	"""
    	return operand_1 - operand_2
